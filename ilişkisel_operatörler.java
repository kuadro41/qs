public class ilişkisel_operatörler {
	public static void main (String[]args) {
		int x = 10;
		int y = 20;
		
		System.out.println(x < y);  // 'x','y'den küçüktür
		System.out.println(x > y);  // 'x','y'den büyüktür
		System.out.println(x >= y); // 'x','y'den büyük ve eşittir
		System.out.println(x <= y); // 'x','y'den küçük ve eşittir
		
		System.out.println(x == y); // 'x','y'ye eşittir
		System.out.println(x != y); // 'x','y'ye eşit değildir
		
		
		boolean b = x !=y ;         // 'x','y'ye eşit değildir 'b'ye atanır
		System.out.println(b);      // atanan 'b' yazdırılır 
		
		
	}
	

}
